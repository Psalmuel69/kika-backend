-- ---------------------------------------------------------------------------
-- Kika ledger schema
-- Zero-signup model: a merchant "account" is created implicitly the first
-- time a WhatsApp number messages the bot. Phone number (E.164) is the
-- natural key.
-- ---------------------------------------------------------------------------

CREATE EXTENSION IF NOT EXISTS pgcrypto;

-- Generic trigger function to keep updated_at fresh — defined first since
-- several tables below attach a trigger to it.
CREATE OR REPLACE FUNCTION set_updated_at() RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = now();
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- ---------------------------------------------------------------------------
-- Subscription tiers — the single source of truth for pricing, currency,
-- billing interval, and marketing feature copy. Every other table that
-- needs to know "which plan" now references this table by id (FK) rather
-- than re-declaring its own CHECK-constrained string enum, so a price
-- change or a new tier is a data update, not a schema/code change.
-- ---------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS subscription_tiers (
    id              INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name            VARCHAR(40)  NOT NULL UNIQUE,        -- 'Free', 'Standard', 'Premium'
    description     TEXT,                                 -- short marketing blurb for the pricing page
    price           NUMERIC(12, 2) NOT NULL DEFAULT 0 CHECK (price >= 0), -- major currency unit (e.g. 5000.00, not kobo)
    currency        VARCHAR(3)   NOT NULL DEFAULT 'NGN' CHECK (currency ~ '^[A-Z]{3}$'),
    "interval"      VARCHAR(10)  NOT NULL DEFAULT 'monthly' CHECK ("interval" IN ('monthly', 'yearly')),
    feature_list    JSONB        NOT NULL DEFAULT '[]',   -- ordered array of perk strings shown on the pricing page
    is_active       BOOLEAN      NOT NULL DEFAULT true,   -- false hides a retired tier from new customers
    display_order   INTEGER      NOT NULL DEFAULT 0,      -- ascending sort position on the pricing page
    created_at      TIMESTAMPTZ  NOT NULL DEFAULT now(),
    updated_at      TIMESTAMPTZ  NOT NULL DEFAULT now(),
    UNIQUE (display_order)
);

CREATE INDEX IF NOT EXISTS idx_subscription_tiers_active_order
    ON subscription_tiers (is_active, display_order);

