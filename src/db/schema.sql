    created_at          TIMESTAMPTZ NOT NULL DEFAULT now(),
    UNIQUE (merchant_id, period_key)
);

CREATE INDEX IF NOT EXISTS idx_digest_cards_public_token ON digest_cards (public_token);
CREATE INDEX IF NOT EXISTS idx_digest_cards_cleanup_sweep
    ON digest_cards (expires_at) WHERE file_deleted_at IS NULL;

-- The full "View Full Report" web page a merchant lands on after tapping
-- through from their Monthly Digest — stored as a data snapshot (not
-- recomputed live) so the report stays consistent even as new ledger
-- entries come in after the digest was sent.
CREATE TABLE IF NOT EXISTS monthly_reports (
    id                  UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    merchant_id         UUID NOT NULL REFERENCES merchants(id) ON DELETE CASCADE,
    period_key          VARCHAR(10) NOT NULL,            -- 'YYYY-MM'
    report_data         JSONB NOT NULL,                  -- full computed snapshot (totals, weekly revenue, debts, top products, AI insight)
    public_token        VARCHAR(64) NOT NULL UNIQUE,
    expires_at          TIMESTAMPTZ NOT NULL,
    created_at          TIMESTAMPTZ NOT NULL DEFAULT now(),
    UNIQUE (merchant_id, period_key)
);

CREATE INDEX IF NOT EXISTS idx_monthly_reports_public_token ON monthly_reports (public_token);

-- Loyalty tracking: one row per (merchant, customer phone). Purchases
