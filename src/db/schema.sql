-- Immutability: audit_logs may only ever be INSERTed into. This is
-- enforced here, at the database level, rather than just "the app code
-- never calls UPDATE/DELETE" (true today, but that's a promise, not a
-- guarantee) — a trigger means even a future bug, a direct psql session,
-- or an admin script can't silently rewrite or erase history. The only
-- way around it is a superuser explicitly disabling the trigger first,
-- which is itself the point: mutation becomes a deliberate, visible act,
-- never an accident.
CREATE OR REPLACE FUNCTION prevent_audit_log_mutation() RETURNS TRIGGER AS $$
BEGIN
    RAISE EXCEPTION 'audit_logs is immutable — % is not permitted (row id=%)', TG_OP, COALESCE(OLD.id, NEW.id);
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trg_audit_logs_immutable_update ON audit_logs;
CREATE TRIGGER trg_audit_logs_immutable_update
    BEFORE UPDATE ON audit_logs
    FOR EACH ROW EXECUTE FUNCTION prevent_audit_log_mutation();

DROP TRIGGER IF EXISTS trg_audit_logs_immutable_delete ON audit_logs;
CREATE TRIGGER trg_audit_logs_immutable_delete
    BEFORE DELETE ON audit_logs
    FOR EACH ROW EXECUTE FUNCTION prevent_audit_log_mutation();

