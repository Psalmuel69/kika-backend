-- ---------------------------------------------------------------------------
-- Access control — blacklist/whitelist by WhatsApp number, and a
-- label-based bot pause so a human agent can take over a conversation
-- without the bot auto-replying underneath them.
-- ---------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS access_control_list (
    id              UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    phone_number    VARCHAR(20) NOT NULL UNIQUE,          -- E.164
    list_type       VARCHAR(10) NOT NULL CHECK (list_type IN ('BLACKLIST', 'WHITELIST')),
    reason          TEXT,
    created_by      VARCHAR(120),                          -- admin identifier who added the rule
    created_at      TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_access_control_phone ON access_control_list (phone_number);
CREATE INDEX IF NOT EXISTS idx_access_control_type ON access_control_list (list_type);

-- Labels applied to a merchant's conversation (e.g. by a support agent in
-- a CRM/handoff tool). While an unresolved "skip" label is active
-- (matched against SKIP_BOT_LABELS), the bot logs the inbound message but
-- does not auto-reply, leaving the human agent in control.
CREATE TABLE IF NOT EXISTS conversation_labels (
    id              UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    merchant_id     UUID NOT NULL REFERENCES merchants(id) ON DELETE CASCADE,
    label           VARCHAR(60) NOT NULL,
    applied_by      VARCHAR(120),
    applied_at      TIMESTAMPTZ NOT NULL DEFAULT now(),
    removed_at      TIMESTAMPTZ                             -- NULL while still active
);

CREATE INDEX IF NOT EXISTS idx_conversation_labels_active
    ON conversation_labels (merchant_id) WHERE removed_at IS NULL;

-- ---------------------------------------------------------------------------
-- Payment links — every digital invoice/checkout link sent inside a
-- WhatsApp chat, across any gateway. `short_url` is the compact link
-- actually texted to the customer (see linkShortenerService); `gateway`
-- and `gateway_reference` tie it back to the underlying Paystack/Stripe/
-- Flutterwave transaction for reconciliation.
-- ---------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS payment_links (
    id                  UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    merchant_id         UUID NOT NULL REFERENCES merchants(id) ON DELETE CASCADE,
    gateway             VARCHAR(20) NOT NULL CHECK (gateway IN ('paystack', 'stripe', 'flutterwave')),
    gateway_reference   VARCHAR(150) NOT NULL,             -- the gateway's own transaction reference
    full_url            TEXT NOT NULL,                      -- the long authorization/checkout URL from the gateway
    short_url           VARCHAR(255) NOT NULL UNIQUE,       -- compact link sent in the WhatsApp text
    short_code          VARCHAR(20) NOT NULL UNIQUE,        -- the path segment of short_url, for fast lookup
    amount_kobo         BIGINT NOT NULL CHECK (amount_kobo >= 0),
    currency            VARCHAR(3) NOT NULL DEFAULT 'NGN',
    customer_phone      VARCHAR(20),
    customer_name       VARCHAR(160),
    description         TEXT,
    status              VARCHAR(16) NOT NULL DEFAULT 'PENDING'
                         CHECK (status IN ('PENDING', 'PAID', 'FAILED', 'EXPIRED', 'CANCELLED')),
    ledger_entry_id     UUID REFERENCES ledger_entries(id) ON DELETE SET NULL, -- set once payment auto-records a CREDIT
    expires_at          TIMESTAMPTZ NOT NULL,                -- when the link becomes invalid
    paid_at             TIMESTAMPTZ,
    created_at          TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at          TIMESTAMPTZ NOT NULL DEFAULT now()
);

