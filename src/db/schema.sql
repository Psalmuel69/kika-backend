-- ---------------------------------------------------------------------------
-- Application/API audit log — every inbound HTTP request the service
-- handles (endpoint, method, status, actor), for security review and
-- operational debugging. Written by the auditLogger middleware on every
-- request; also used for explicit business-event audit entries (e.g.
-- "merchant.blacklisted", "payment_link.created") via auditLogService.
-- ---------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS audit_logs (
    id              UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    merchant_id     UUID REFERENCES merchants(id) ON DELETE SET NULL,
    actor_type      VARCHAR(20) NOT NULL DEFAULT 'SYSTEM' CHECK (actor_type IN ('MERCHANT', 'SYSTEM', 'ADMIN', 'WEBHOOK')),
    actor_id        VARCHAR(120),                -- phone number, admin username, or 'system'
    action          VARCHAR(100) NOT NULL,       -- e.g. 'ledger_entry.create', 'payment_link.create', 'merchant.blacklist'
    endpoint        VARCHAR(150),
    http_method     VARCHAR(10),
    status_code     INTEGER,
    is_success      BOOLEAN NOT NULL DEFAULT true,
    request_id      VARCHAR(64),
    ip_address      VARCHAR(45),
    metadata        JSONB NOT NULL DEFAULT '{}',
    created_at      TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_audit_logs_merchant ON audit_logs (merchant_id, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_audit_logs_action ON audit_logs (action);
CREATE INDEX IF NOT EXISTS idx_audit_logs_created_at ON audit_logs (created_at DESC);

