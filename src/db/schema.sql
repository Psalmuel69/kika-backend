-- Tracks which merchants have already received a given day's Sunset
-- Report or a given month's Insights report, so a retried/duplicated
-- scheduler tick can never send the same report twice.
CREATE TABLE IF NOT EXISTS report_dispatch_log (
    id                  UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    merchant_id         UUID NOT NULL REFERENCES merchants(id) ON DELETE CASCADE,
    report_type         VARCHAR(24) NOT NULL CHECK (report_type IN ('DAILY_SUNSET', 'MONTHLY_INSIGHTS', 'MONTHLY_DIGEST', 'FRIDAY_AMNESTY_PROMPT')),
    period_key          VARCHAR(10) NOT NULL,             -- 'YYYY-MM-DD' or 'YYYY-MM'
    sent_at             TIMESTAMPTZ NOT NULL DEFAULT now(),
    UNIQUE (merchant_id, report_type, period_key)
);

-- Generated receipt card assets (rendered PNGs served over a signed URL).
CREATE TABLE IF NOT EXISTS receipts (
    id                  UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    merchant_id         UUID NOT NULL REFERENCES merchants(id) ON DELETE CASCADE,
    ledger_entry_id      UUID REFERENCES ledger_entries(id) ON DELETE SET NULL,
    file_path           TEXT NOT NULL,
    public_token        VARCHAR(64) NOT NULL UNIQUE,   -- random, unguessable path segment
    expires_at          TIMESTAMPTZ NOT NULL,
    file_deleted_at     TIMESTAMPTZ,                    -- set once the on-disk file has been pruned (after expires_at); NULL while the file still exists
    created_at          TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_receipts_public_token ON receipts (public_token);
CREATE INDEX IF NOT EXISTS idx_receipts_cleanup_sweep
    ON receipts (expires_at) WHERE file_deleted_at IS NULL;

