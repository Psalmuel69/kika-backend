-- Every sale / expense / debt entry a merchant records via chat.
-- `total_kobo` is the full value of the transaction; `paid_kobo` is what was
-- actually handed over at the time; `balance_kobo` is what's still owed
-- (0 for a fully-paid CREDIT/DEBIT). This three-way split mirrors how
-- merchants actually talk: "she pay 15k remain 12k" on a 27k sale.
CREATE TABLE IF NOT EXISTS ledger_entries (
    id                  UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    merchant_id         UUID NOT NULL REFERENCES merchants(id) ON DELETE CASCADE,
    entry_type          VARCHAR(20) NOT NULL CHECK (entry_type IN ('CREDIT', 'DEBIT', 'DEBT', 'DEBT_SETTLEMENT')),
    counterparty_name   VARCHAR(160),                    -- customer/supplier name, optional
    counterparty_phone  VARCHAR(20),                     -- optional E.164 customer number, for loyalty tracking
    description         TEXT NOT NULL,
    items               JSONB NOT NULL DEFAULT '[]',     -- [{name, quantity, unit}], best-effort extraction
    total_kobo          BIGINT NOT NULL CHECK (total_kobo >= 0),
    paid_kobo           BIGINT NOT NULL DEFAULT 0 CHECK (paid_kobo >= 0),
    balance_kobo        BIGINT NOT NULL DEFAULT 0 CHECK (balance_kobo >= 0),
    balance_after_kobo  BIGINT,                          -- customer's rolling debt balance snapshot immediately after this entry (DEBT/DEBT_SETTLEMENT only) — computed under row lock, see customer_balances
    currency            VARCHAR(3) NOT NULL DEFAULT 'NGN',
    is_settled          BOOLEAN NOT NULL DEFAULT false,  -- true once balance_kobo hits 0
    is_voided           BOOLEAN NOT NULL DEFAULT false,  -- true once undone via the UNDO command
    voided_at           TIMESTAMPTZ,
    raw_message         TEXT,                            -- original WhatsApp text, for audit
    receipt_id          UUID,                             -- set once a receipt card is generated
    receipt_decision_pending BOOLEAN NOT NULL DEFAULT true, -- true until the merchant has said yes/no to "want a receipt for this?" (see worker.js's DONE-triggered confirmation flow); flipped false either way once resolved
    created_at          TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- Defensive backfill, run BEFORE the indexes below reference this column
-- — see the identical comment on subscription_tiers.price_yearly above
-- for why this matters (a database migrated before this column existed
-- would otherwise fail the very next CREATE INDEX statement and, since
-- migrate.js runs this whole file as one implicit transaction, abort
-- the entire migration).
ALTER TABLE ledger_entries ADD COLUMN IF NOT EXISTS receipt_decision_pending BOOLEAN NOT NULL DEFAULT true;

CREATE INDEX IF NOT EXISTS idx_ledger_entries_merchant_created
    ON ledger_entries (merchant_id, created_at DESC) WHERE is_voided = false;
CREATE INDEX IF NOT EXISTS idx_ledger_entries_outstanding_debt
    ON ledger_entries (merchant_id, is_settled) WHERE balance_kobo > 0 AND is_voided = false;
CREATE INDEX IF NOT EXISTS idx_ledger_entries_items_gin
    ON ledger_entries USING GIN (items);
CREATE INDEX IF NOT EXISTS idx_ledger_entries_counterparty_phone
    ON ledger_entries (merchant_id, counterparty_phone) WHERE counterparty_phone IS NOT NULL;
CREATE INDEX IF NOT EXISTS idx_ledger_entries_receipt_decision_pending
    ON ledger_entries (merchant_id, created_at) WHERE receipt_decision_pending = true AND is_voided = false;

-- ---------------------------------------------------------------------------
-- Customer balances — the single authoritative "account" row per
-- (merchant, customer) holding their current rolling debt balance.
--
-- This exists specifically to eliminate a race condition: ledger_entries
-- rows are independent inserts, so two "sales on credit" to the SAME
-- customer arriving within seconds of each other (two concurrent worker
-- jobs) have nothing to serialize against one another on if the rolling
-- total is only ever computed by SUMming ledger_entries at read time.
-- Every debt-affecting write (a new DEBT, or a DEBT_SETTLEMENT payment)
-- must go through queries.lockCustomerBalance() first, which:
--   1. INSERTs the row if it doesn't exist yet (ON CONFLICT DO NOTHING —
--      itself race-safe: if two transactions both try to create a brand
--      new customer's first row concurrently, only one insert wins)
--   2. SELECTs it back with FOR UPDATE inside the caller's transaction
-- A second concurrent transaction touching the same customer blocks on
-- that FOR UPDATE until the first COMMITs, then reads the up-to-date
-- balance — guaranteeing no lost updates regardless of how many sales
-- land for the same customer at the same instant.
-- ---------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS customer_balances (
    id                   UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    merchant_id          UUID NOT NULL REFERENCES merchants(id) ON DELETE CASCADE,
    counterparty_name    VARCHAR(160) NOT NULL,
    rolling_balance_kobo BIGINT NOT NULL DEFAULT 0 CHECK (rolling_balance_kobo >= 0),
    created_at           TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at           TIMESTAMPTZ NOT NULL DEFAULT now(),
    UNIQUE (merchant_id, counterparty_name)
);

CREATE INDEX IF NOT EXISTS idx_customer_balances_merchant
    ON customer_balances (merchant_id);

DROP TRIGGER IF EXISTS trg_customer_balances_updated_at ON customer_balances;
CREATE TRIGGER trg_customer_balances_updated_at
    BEFORE UPDATE ON customer_balances
    FOR EACH ROW EXECUTE FUNCTION set_updated_at();

-- ---------------------------------------------------------------------------
-- Products / inventory — opt-in stock tracking. A product only exists
-- here once a merchant explicitly registers it via "ADD STOCK: name, qty".
-- Sales that mention a matching item name (case-insensitive) decrement
-- current_stock; crossing below low_stock_threshold fires the Low Stock
-- Alert appended to that sale's receipt.
-- ---------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS products (
    id                   UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    merchant_id          UUID NOT NULL REFERENCES merchants(id) ON DELETE CASCADE,
    name                 VARCHAR(160) NOT NULL,
    unit                 VARCHAR(40),
    current_stock        NUMERIC(12, 2) NOT NULL DEFAULT 0 CHECK (current_stock >= 0),
    low_stock_threshold  NUMERIC(12, 2) NOT NULL DEFAULT 5,
    created_at           TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at           TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- Case-insensitive uniqueness ("Rice" and "rice" are the same product)
-- via an expression index rather than a plain UNIQUE constraint.
CREATE UNIQUE INDEX IF NOT EXISTS idx_products_merchant_name_ci
    ON products (merchant_id, lower(name));

DROP TRIGGER IF EXISTS trg_products_updated_at ON products;
CREATE TRIGGER trg_products_updated_at
    BEFORE UPDATE ON products
    FOR EACH ROW EXECUTE FUNCTION set_updated_at();

